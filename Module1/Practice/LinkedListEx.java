package pra178;

import java.util.LinkedList;

public class LinkedListEx 
{
public static void main(String[] args) {
		
		LinkedList ll = new LinkedList<>();
		
		ll.add("YASH");
		
		ll.add(20);
		
		ll.add('K');
		
		System.out.println(ll);
		
		ll.addFirst("HELLO");
		
		ll.addLast("BYEE");
		
		System.out.println(ll);
}
}